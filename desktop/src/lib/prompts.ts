const KEY = 'gcms.pilot.prompts';

export function loadUserPrompts(): Prompt[] {
  try {
    const raw = localStorage.getItem(KEY);
    if (raw) {
      const arr = JSON.parse(raw);
      if (Array.isArray(arr)) {
        return arr
          .filter((p) => p && typeof p.title === 'string' && typeof p.body === 'string')
          .map((p) => ({ id: String(p.id ?? newPromptId()), title: p.title, body: p.body, created_at: p.created_at }));
      }
    }
  } catch {
    /* ignore */
  }
  return [];
}

/** 成功返回 true；本地存储写入失败（如配额超限）返回 false，由调用方提示用户。 */
export function saveUserPrompts(list: Prompt[]): boolean {
  try {
    localStorage.setItem(KEY, JSON.stringify(list.map((p) => ({ id: p.id, title: p.title, body: p.body, created_at: p.created_at }))));
    return true;
  } catch {
    return false;
  }
}

export function newPromptId(): string {
  return 'u' + Date.now().toString(36) + Math.random().toString(36).slice(2, 7);
}
